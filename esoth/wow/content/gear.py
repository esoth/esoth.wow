slot = {}
gear = {}

for g in _gear:
  for i in g['ilvls']:
    name = '%s (%d)' % (g['name'],i)
    if boss.has_key(g['boss']):
      if boss[ g['boss'] ].has_key(g['slot']):
        boss[ g['boss'] ][ g['slot'] ].append(name)
      else:
        boss[ g['boss'] ][ g['slot'] ] = [name]
    else:
      boss[ g['boss'] ] = {g['slot']:[name]}
    
    if slot.has_key(g['slot']):
      slot[ g['slot'] ].append(name)
    else:
      slot[ g['slot'] ]=[name]

    item = {'boss':g['boss'],'slot':g['slot'],'id':g.get('id')}
    gear[name] = item

